import turtle

pen = turtle.Turtle()

pen.forward(100)
pen.right(90)     # Rotate clockwise by 90 degrees

pen.forward(100)
pen.right(90)

pen.forward(100)
pen.right(90)

pen.forward(300)
pen.left(90)

pen.forward(100)
pen.left(90)

pen.forward(300)
pen.right(90)

pen.forward(100)
pen.right(90)

pen.forward(100)
pen.right(90)

pen.forward(100)


turtle.done()